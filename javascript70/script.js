function bb1() {
    let a = Math.floor(Math.random() * 3);

    switch (a) {
        case 0:
            document.getElementById("box1").style.color = "green";
            break;
        case 1:
            document.getElementById("box1").style.color = "yellow";
            break;
        case 2:
            document.getElementById("box1").style.color = "red";
            break;
    }

    let b = Math.floor(Math.random() * 3);
    switch (b) {
        case 0:
            document.getElementById("box1").style.backgroundColor = "aqua";
            break;
        case 1:
            document.getElementById("box1").style.backgroundColor = "pink";
            break;
        case 2:
            document.getElementById("box1").style.backgroundColor = "blue";
            break;
    }
}

